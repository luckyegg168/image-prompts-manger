SUPPORTED_LOCALES = list(TRANSLATIONS.keys())
DEFAULT_LOCALE = "zh-TW"


class I18n:
    """輕量多語系管理器"""

    def __init__(self, locale: str = DEFAULT_LOCALE):
        self.locale = locale if locale in TRANSLATIONS else DEFAULT_LOCALE

    def t(self, key: str, **kwargs) -> str:
        """取得翻譯字串，支援 {placeholder} 格式"""
        text = TRANSLATIONS[self.locale].get(key)
        if text is None:
            # Fallback to zh-TW
            text = TRANSLATIONS[DEFAULT_LOCALE].get(key, key)
        if kwargs:
            try:
                text = text.format(**kwargs)
            except (KeyError, ValueError):
                pass
        return text

    def set_locale(self, locale: str):
        if locale in TRANSLATIONS:
            self.locale = locale

    @property
    def locale_label(self) -> str:
        labels = {"zh-TW": "繁中", "en-US": "EN", "ja-JP": "JA"}
        return labels.get(self.locale, self.locale)
